// Strings
public class Strings {
	public static void main(String[] args) {
		String message_1 = "Felipe";
		String message_2 = "01234";
		System.out.println(message_1);
		System.out.println(message_1.length());

		System.out.println(message_1.indexOf("e"));
		System.out.println(message_1.replace("Felipe", "Serna"));

		System.out.println(message_2);
		System.out.println(message_2.length());
	}
}
